def _print_apply(args) -> int:
    plan = InstallPlan.from_json(Path(args.plan).read_text(encoding="utf-8"))
    payload = {
        "dry_run": True,
        "applied": False,
        "install_plan": plan.to_dict(),
        "warnings": ["Apply is validate-only in phase 10; no files were written."],
    }
    if args.json:
        _print_json(payload)
    else:
        print(f"Plan project: {plan.project_root}")
        print(f"Workflow: {plan.workflow}")
        print(f"Packs: {', '.join(plan.packs) if plan.packs else 'none'}")
