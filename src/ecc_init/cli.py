def _workflow_result_payload(result) -> dict[str, object]:
    return {
        "workflow": result.workflow_id,
        "status": result.status,
        "checks": [check.__dict__ for check in result.checks],
        "commands": [
            {"args": list(command.args), "description": command.description, "dry_run": command.dry_run}
            for command in result.commands
        ],
        "warnings": list(result.warnings),
    }


def _print_workflow_status(path: Path, json_output: bool) -> int:
    result = GsdWorkflowAdapter().verify(AppPaths.build(path))
    if json_output:
        _print_json(_workflow_result_payload(result))
    else:
        print(f"Workflow: {result.workflow_id}")
        print(f"Status: {result.status}")
        for check in result.checks:
            print(f"[{'PASS' if check.ok else 'WARN'}] {check.check_id}: {check.message} {check.detail}".rstrip())
        print("[hint] Prefer `ecc-init gsd status` for device/runtime-level GSD checks.")
    return 0 if result.ok else 3


def _print_gsd(args) -> int:
    paths = AppPaths.build(Path(args.path))
    adapter = GsdWorkflowAdapter()
    effective_dry_run = args.dry_run or not args.yes
    options = GsdInstallOptions(
        runtime=args.runtime,
        scope=args.scope,
        version=args.version,
        yes=args.yes,
        dry_run=effective_dry_run,
    )
    if args.gsd_command == "status":
        result = adapter.status(paths, runtime=args.runtime, scope=args.scope)
    elif args.gsd_command == "verify":
        result = adapter.verify(paths, runtime=args.runtime, scope=args.scope)
    elif args.gsd_command == "install":
        result = adapter.install(paths, options)
    elif args.gsd_command == "update":
        result = adapter.update(paths, options)
    else:
        raise ValueError(f"unknown gsd command: {args.gsd_command}")

    if args.json:
        _print_json(_workflow_result_payload(result))
    else:
        print(f"GSD Core: {result.status}")
        print(f"Runtime: {args.runtime}")
        print(f"Scope: {args.scope}")
        if result.commands:
            print("Command: " + " ".join(result.commands[0].args))
            print("Mode: " + ("dry-run" if result.commands[0].dry_run else "apply"))
        for check in result.checks:
            print(f"[{'PASS' if check.ok else 'FAIL'}] {check.check_id}: {check.message} {check.detail}".rstrip())
        for warning in result.warnings:
            print(f"[warning] {warning}")
    if result.status == "not_installed":
        return 2
    if result.status == "blocked_environment":
        return 3
    return 0 if result.ok else 1


