def _normalize_argv(argv: list[str]) -> list[str]:
    if not argv:
        return ["init"]
    if argv[0] in COMMANDS or argv[0] in {"--version", "-h", "--help"}:
        return argv
    if argv[0] in GLOBAL_FLAGS:
        head: list[str] = []
        rest = list(argv)
        while rest and rest[0] in GLOBAL_FLAGS:
            head.append(rest.pop(0))
        if not rest or rest[0] not in COMMANDS:
            return [*head, "init", *rest]
        return [*head, *rest]
    return ["init", *argv]


def _print_run_report(report) -> None:
    print(f"Project: {report.project_root}")
    print("Detected stacks: " + (", ".join(report.detection.stacks) if report.detection.stacks else "none"))
    if report.upstream_ref:
        print(f"ECC upstream ref: {report.upstream_ref}")
    counts: dict[str, int] = {}
    for result in report.results:
        counts[result.status] = counts.get(result.status, 0) + 1
        if result.status in {"conflict", "preserved"}:
            print(f"[needs attention] {result.path}: {result.message}")
    print("File results: " + ", ".join(f"{key}={value}" for key, value in sorted(counts.items())))
    if report.backup_id:
        print(f"Backup: {report.backup_id}")
    for warning in report.warnings:
        print(f"[warning] {warning}")
    if report.conflicts:
        print("Conflicts were preserved locally; inspect generated upstream/diff files.")


