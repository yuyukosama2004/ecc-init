def _print_json(payload: object) -> None:
    text = json.dumps(payload, ensure_ascii=False, indent=2)
    stdout_buffer = getattr(sys.stdout, "buffer", None)
    if stdout_buffer is None:
        print(text)
        return
    stdout_buffer.write((text + "\n").encode("utf-8"))
    stdout_buffer.flush()


