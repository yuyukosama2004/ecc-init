def _doctor_payload(path: Path) -> dict[str, object]:
    checks = []
    summary = {"PASS": 0, "WARN": 0, "FAIL": 0}
    hard_fail_indexes = {0, 2, 3, 4, 5}
    for index, (label, ok, detail) in enumerate(doctor(path)):
        status = "PASS" if ok else ("FAIL" if index in hard_fail_indexes else "WARN")
        summary[status] += 1
        checks.append(
            {
                "check_id": f"doctor:{index}",
                "label": label,
                "status": status,
                "ok": ok,
                "detail": detail,
            }
        )
    return {"project_root": str(Path(path).expanduser().resolve()), "summary": summary, "checks": checks}


def _print_doctor(args) -> int:
    payload = _doctor_payload(Path(args.path))
    if args.json:
        _print_json(payload)
    else:
        for check in payload["checks"]:
            print(f"[{check['status']}] {check['label']}: {check['detail']}")
    return 3 if payload["summary"]["FAIL"] else 0


def _print_status(args) -> int:
    status = project_status(Path(args.path))
    if args.json:
        _print_json(status)
        return 0
    print(f"Project: {status['project_root']}")
    print("Detected stacks: " + (", ".join(status["detected_stacks"]) or "none"))
    print("Global CLAUDE.md: " + ("present" if status["global_claude_md"] else "missing"))
    print("Global skills: " + (", ".join(status["global_skills"]) or "none"))
    print("Project skills: " + (", ".join(status["project_skills"]) or "none"))
    print("Code tour completed: " + ("yes" if status["code_tour_completed"] else "no"))
    print(f"Upstream ref: {status['upstream_ref'] or 'unknown'}")
    print(f"Last initialized: {status['last_initialized_at'] or 'never'}")
    print(f"Backup count: {status['backup_count']}")
    if status["conflicts"]:
        print("Conflict files:")
        for item in status["conflicts"]:
            print(f"- {item}")
    if status["modified_managed_files"]:
        print("Modified managed files:")
        for item in status["modified_managed_files"]:
            print(f"- {item}")
    return 0


def _print_update(args) -> int:
    report = update_project(
        Path(args.path),
        profile_id=args.profile,
        include_packs=args.pack,
        exclude_packs=args.without_pack,
        update_sources=args.sources,
        update_workflow=args.workflow,
        update_packs=args.packs,
        source_ids=args.source,
        check=args.check,
        dry_run=args.dry_run,
        yes=args.yes,
    )
    if args.json:
        _print_json(report.to_dict())
    else:
        print(f"Project: {report.project_root}")
        print("Mode: " + ("dry-run" if report.dry_run else "apply"))
        print(f"Sources checked: {len(report.source_checks)}")
        print(f"Workflow update: {report.workflow_result.status if report.workflow_result else 'not requested'}")
        print(f"Pack update changed: {report.config_report.changed if report.config_report else False}")
        if report.operation_id:
            print(f"Operation: {report.operation_id}")
        for warning in report.warnings:
            print(f"[warning] {warning}")
        for item in report.conflicts:
            print(f"[conflict] {item}")
        for item in report.modified_managed_files:
            print(f"[modified] {item}")
    if not report.source_ok:
        return 1
    if report.manual_action_required:
        return 2
    if report.workflow_result and not report.workflow_result.ok and not report.dry_run:
        return 1
    return 0


